    /*
     *Este método identifica al usuario (Médico) que quiere ingresar al sistema, partiendo de un correo y una clave. Se valida
     *e ingresa a la ventana MenuMedico.
     */
    public void validarUsuario() {

        int resultado = 0;
        String pass = String.valueOf(txtPassword.getPassword());
        String email = txtUser.getText();
        String SQL = "SELECT *FROM registro_medicos WHERE EmailMedico = '" + email + "' AND passMedico = '" + pass + "';";

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);

            if (rs.next()) {
                resultado = 1;

                if (resultado == 1) {

                    MenuMedico menuM = new MenuMedico();
                    menuM.setVisible(true);
                    this.dispose();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Error de Acceso. Email o contraseña incorrectos.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex.getMessage());
        }

    }

