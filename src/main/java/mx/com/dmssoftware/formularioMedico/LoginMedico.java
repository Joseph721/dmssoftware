    private void validarCampos() {
        if (txtUser.getText().isEmpty() || txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No puede dejar campos sin completar");
        } else {

            validarUsuario();
            limpiarCampos();

        }
    }

    private void limpiarCampos() {
        txtUser.setText("");
        txtPassword.setText("");
    }
